public class Electeur {
    String nom;
    String telephone;
    String sexe;
    String adresse;
    String codePostal;

    public Electeur(String nom, String telephone, String sexe, String adresse, String codePostal) {
        this.nom = nom;
        this.telephone = telephone;
        this.sexe = sexe;
        this.adresse = adresse;
        this.codePostal = codePostal;
    }
}
